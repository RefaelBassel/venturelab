export function detectProjectType(args: {
  ventureName: string;
  problem: string;
  vision: string;
}): ProjectType | null {
  const text = `${args.ventureName} ${args.problem} ${args.vision}`.toLowerCase();
  let best: { type: ProjectType; score: number } | null = null;
  for (const type of PROJECT_TYPES) {
    let score = 0;
    for (const kw of type.keywords) {
      if (text.includes(kw.toLowerCase())) score++;
    }
    if (score > 0 && (!best || score > best.score)) {
      best = { type, score };
    }
  }
  return best ? best.type : null;
}
